from textwrap import dedent


SYSTEM_PROMPT = dedent(
    """
    너는 한국어 웹소설의 시놉시스를 읽고 작품 관리용 캐릭터 설정집을 만드는 분석가다.
    반드시 JSON만 반환한다.
    필드명은 지정된 JSON 형식을 절대 변경하지 않는다.
    원문 근거가 부족한 값은 빈 문자열로 둔다.
    특정 작품, 특정 캐릭터 이름, 특정 테스트 데이터를 기준으로 값을 만들지 않는다.
    """
).strip()


def build_character_extract_prompt(*, work_title: str, genre: str, synopsis: str, limit: int) -> str:
    return dedent(
        f"""
        [작품 정보]
        제목: {work_title or '작품'}
        장르: {genre or '장르 미입력'}

        [시놉시스]
        {synopsis}

        [반환 JSON 형식]
        {{
          "characters": [
            {{
              "char_name": "필수, 30자 이내",
              "age": "선택, 10자 이내",
              "role": "필수, 10자 이내",
              "profile_label": "관계도 카드 표시용 라벨, 80자 이내",
              "gender": "필수, M/F/U 중 하나",
              "relationships": "선택, 500자 이내",
              "appearance": "선택, 300자 이내",
              "detail_setting": "선택, 1000자 이내"
            }}
          ]
        }}

        [필드 작성 기준]
        - char_name: 캐릭터 이름이다.
          반드시 있어야 한다.
          이름이 없는 인물 항목은 만들지 않는다.

        - age: 나이다.
          시놉시스에서 명확하게 확인되지 않으면 빈 문자열로 둔다.

        - role: 캐릭터의 작품 내 등장 비중과 서사 영향도다.
          반드시 주연, 조연, 단역 중 하나만 작성한다.
          주연: 작품의 중심 시점, 중심 감정선, 핵심 선택, 갈등 해결을 직접 담당하는 인물이다.
          조연: 주연의 목표, 갈등, 비밀, 대립 구도에 지속적으로 영향을 주지만 중심 시점과 핵심 선택의 주체는 아닌 인물이다.
          단역: 특정 사건이나 배경 설명에 제한적으로 등장하며 핵심 갈등을 직접 움직이지 않는 인물이다.
          주연은 기본적으로 1명 또는 2명으로 판단한다.
          로맨스, 로맨스판타지, 버디물, 투톱 구조처럼 두 인물이 중심 서사를 함께 이끌 때만 2명을 주연으로 분류할 수 있다.
          3명 이상을 주연으로 분류하는 것은 군상극, 다중 주인공, 팀 주연 구조가 시놉시스에 명확할 때만 허용한다.
          단순히 주인공의 연인, 약혼자, 조력자, 보호자, 정보 제공자라는 이유만으로 주연으로 분류하지 않는다.
          핵심 사건을 일으키거나 중요한 비밀을 가진 인물이라도 중심 시점과 핵심 선택을 담당하지 않으면 조연으로 분류한다.
          악역, 배후 인물, 감시자, 조력자, 정보 제공자, 보호자는 서사 영향도가 커도 기본적으로 조연으로 분류한다.
          주연을 직접 조종하거나, 기억 조작, 배신, 감시, 살해 은폐, 권력 갈등처럼 핵심 사건을 일으키는 인물은 단역으로 분류하지 않는다.
          악역, 조력자, 연인, 가족, 친구, 직업, 신분, 작위, 계급은 role에 쓰지 않는다.
          악역, 조력자, 약혼자, 가족 같은 기능은 relationships 또는 detail_setting에 반영한다.
          황후, 왕세자, 기사, 시녀, 공작, 마법사 같은 신분, 직업, 작위는 profile_label에만 반영한다.

        - profile_label: 관계도 카드 하단에 표시할 짧은 라벨이다.
          인물의 직업, 신분, 작위, 공식 직책, 계급, 소속 내 위치처럼 관계도에서 인물을 구분하는 표시값을 작성한다.

          profile_label은 role과 다르다.
          role은 주연/조연/단역 기준의 등장 비중이고, profile_label은 인물의 사회적 위치나 직업적 표시값이다.

          profile_label에 주연, 조연, 단역, 주인공, 조력자, 악역, 라이벌, 동료 같은 작품 내 역할을 쓰지 않는다.
          profile_label에 남성, 여성, 미상 같은 성별을 쓰지 않는다.
          profile_label에 친구, 연인, 가족, 원수, 스승, 제자 같은 관계 표현만 쓰지 않는다.

          시놉시스에서 인물의 직업, 신분, 작위, 공식 직책, 계급, 소속 내 위치가 확인되면 profile_label을 반드시 작성한다.
          명확한 사회적 위치나 직업적 표시값이 전혀 확인되지 않을 때만 빈 문자열로 둔다.

        - gender: 성별 코드다.
          반드시 M, F, U 중 하나로만 작성한다.
          M: 남성으로 판단되는 인물
          F: 여성으로 판단되는 인물
          U: 성별 근거가 부족하거나 불명확한 인물

          gender에 남, 여, 미상, 남성, 여성, 기타 같은 한글 값을 쓰지 않는다.
          gender에 직업, 신분, 작위, 공식 직책, 계급, 소속 정보를 쓰지 않는다.

        - relationships: 다른 주요 인물과의 관계만 요약한다.
          인물의 전체 줄거리나 개인 설정을 쓰지 말고, 관계 대상과 관계 성격이 드러나게 작성한다.
          가능한 한 2문장 이내로 작성한다.
          반드시 인물명을 포함해 누가 누구와 어떤 관계인지 드러나게 쓴다.
          사건 설명보다 관계의 성격을 우선한다.
          근거가 부족하면 빈 문자열로 둔다.

        - appearance: 외형 정보를 요약한다.
          시놉시스에서 확인되는 외모, 복장, 분위기, 인상만 작성한다.
          근거가 부족하면 빈 문자열로 둔다.

        - detail_setting: 인물 자체의 설정을 설정집 표시용으로 정제한다.
          성격, 말투, 배경 서사, 목표, 갈등, 능력, 약점 등을 요약하되 시놉시스에서 확인 가능한 내용만 작성한다.
          가능한 한 2~3문장으로 작성한다.
          문장 순서는 가능한 한 현재 신분/위치, 과거 또는 비밀, 현재 목표/갈등 순서로 작성한다.
          한 문장에 사건을 과도하게 나열하지 않는다.
          관계 정보는 relationships에 우선 작성하고, detail_setting에는 인물 자체의 설정을 중심으로 쓴다.
          추측한 설정, 원문에 없는 직업, 신분, 작위, 계급은 추가하지 않는다.
          profile_label에 들어가야 할 정보를 detail_setting에만 쓰고 profile_label을 비워두면 안 된다.

        [강제 규칙]
        - 주요 인물에게 직업, 신분, 작위, 공식 직책, 계급, 소속 내 위치가 보이면 profile_label을 절대 비우지 않는다.
        - profile_label은 관계도 표시용 DB 컬럼에 저장될 값이므로 반드시 별도 필드로 작성한다.
        - profile_label 정보를 detail_setting에만 쓰고 profile_label 필드를 비워두면 안 된다.
        - role은 반드시 주연, 조연, 단역 중 하나만 사용한다.
        - 주연은 기본 1~2명으로 제한하고, 3명 이상은 군상극/다중 주인공 구조가 명확할 때만 허용한다.
        - role과 profile_label을 같은 값으로 쓰지 않는다.
        - gender는 반드시 M/F/U 코드만 사용한다.
        - 특정 작품, 특정 캐릭터 이름, 특정 테스트 데이터를 기준으로 profile_label을 만들지 않는다.
        - 시놉시스에서 확인 가능한 정보만 사용한다.
        - 원문 근거가 부족한 값은 빈 문자열로 둔다.

        [추출 규칙]
        - 최대 {limit}명까지만 추출한다.
        - 이름이 없는 항목은 만들지 않는다.
        - 작품 속 인물이 아닌 추상 개념은 캐릭터로 만들지 않는다.
        - 단역이나 엑스트라는 제외하고 주요 인물 위주로 추출한다.
        - 같은 인물의 본명, 별명, 호칭은 하나의 캐릭터로 병합한다.
        - 내용은 모두 한국어로 작성한다.
        - 과장해서 새 설정을 만들지 말고 시놉시스에서 확인 가능한 내용만 정리한다.
        - 반드시 JSON만 반환하고, 설명 문장은 붙이지 않는다.
        """
    ).strip()
